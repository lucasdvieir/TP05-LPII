private void iniciarThreads() {
        int maximo = Integer.parseInt(jTextField1.getText());
        int prioridade1 = Integer.parseInt(jTextField2.getText());
        int prioridade2 = Integer.parseInt(jTextField3.getText());

        Thread thread1 = new Thread(new MyRunnable(jTextArea1, jLabel5, maximo), "Thread 1");
        thread1.setPriority(prioridade1);

        Thread thread2 = new Thread(new MyRunnable(jTextArea2, jLabel6, maximo), "Thread 2");
        thread2.setPriority(prioridade2);

        thread1.start();
        thread2.start();
    }

    private void limparCampos() {
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextArea1.setText("");
        jTextArea2.setText("");
        jLabel5.setVisible(false);
        jLabel6.setVisible(false);
    }
